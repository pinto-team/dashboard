type Join<K, P> = K extends string ? (P extends string ? `${K}.${P}` : never) : never

type Paths<T> = {
    [K in keyof T]: T[K] extends Record<string, unknown>
        ? K extends string
            ? T[K] extends Record<string, never>
                ? K
                : K | Join<K, Paths<T[K]>>
            : never
        : K
}[keyof T]

export type LocaleKey = Paths<typeof en>
